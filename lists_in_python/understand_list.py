# Understanding lists in Python

# Lists in Python are ordered collections of items that can be of any data type.
list1 = [11, 12, 13, 14, 15]* 2

print(list1[::1])


# Lists can contain different data types, including strings, integers, and even other lists.


list2 =['A','B','C','D']

for index, j in enumerate(list2):
    print(f"index: {index}, value: {j}")


